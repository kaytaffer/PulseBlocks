void setBit(uint8_t *target, int bit, char setTo)
{
    *target = *target & ~(1 << bit);
    *target += (int) setTo << bit;
}


void convertPixels(char arr[DROWS][DCOLS], uint8_t screen[512]) {
    int r, c;
    int scElement = 127;
    int bit;
    int ofs;
    for (r = 0; r < DROWS; r++) { // go though rows
        bit = 0;
        ofs = 0;
        for (c = 0; c < DCOLS; c++) { // go through cols
            setBit(&screen[scElement + ofs * 128], bit, arr[r][c]);
            bit++;
            if(bit == 8) {
                bit = 0;
                ofs++;
            }
        }
        scElement--;
    }
}


